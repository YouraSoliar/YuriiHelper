package com.example.yuriihelper;

public class ItemCurrency {
    String name;
    String average;
    String nbu;
    String blackMarket;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAverage() {
        return average;
    }

    public void setAverage(String average) {
        this.average = average;
    }

    public String getNbu() {
        return nbu;
    }

    public void setNbu(String nbu) {
        this.nbu = nbu;
    }

    public String getBlackMarket() {
        return blackMarket;
    }

    public void setBlackMarket(String blackMarket) {
        this.blackMarket = blackMarket;
    }
}
